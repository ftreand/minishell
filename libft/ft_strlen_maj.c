#include "libft.h"

size_t	ft_strlen_maj(char *s)
{
	int i;

	i = 0;
	if ((s[i] < 65 && s[i] > 90) || !s )
		return (0);
	while (s[i] && s[i] >= 65 && s[i] <= 90)
		i++;
	return (i);
}
